void main() {
  var bdCity = ['Sylhet', 'Comilla', 'Dhaka', 'Rajshahi', 'Khulna', 'Rangpur', 'Chittagong'];

  // DART LIST PROPERTIES
  // first
  var result = bdCity.first;
  print(result);

  // inEmpty
  print(bdCity.isEmpty);

  // length
  print(bdCity.length);

  // last
  print(bdCity.last);

  // reverse
  print(bdCity.reversed);

  // single =  check whether it contains only one element or not
  print(bdCity.singleOrNull);

}